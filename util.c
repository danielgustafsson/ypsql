/* PETSCII -> ASCII */
void
_a(char *a)
{
	char *p = a;
	while(*p)
	{
		*p = ascii[*p];
		p++;
	}
}

/* ASCII -> PETSCII */
void
_p(char *p)
{
	char *ptr = p;
	while(*ptr)
	{
		*ptr = petscii[*ptr];
		ptr++;
	}
}
